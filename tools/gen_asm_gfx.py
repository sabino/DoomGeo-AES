def encode_fix_pixels(pixels: list[int]) -> bytes:
    data = bytearray()
    for xa, xb in ((4, 5), (6, 7), (0, 1), (2, 3)):
        for y in range(8):
            pixel_a = pixels[y * 8 + xa] & 0x0F
            pixel_b = (pixels[y * 8 + xb] & 0x0F) << 4
            data.append(pixel_b | pixel_a)
    return bytes(data)


def encode_fix_glyph(rows: tuple[int, ...]) -> bytes:
    pixels = [0] * 64
    for y, bits in enumerate(rows):
        for x in range(8):
            if bits & (0x80 >> x):
                pixels[y * 8 + x] = 15
    return encode_fix_pixels(pixels)


def encode_tile(px: list[list[int]]) -> tuple[bytes, bytes]:
    c1, c2 = bytearray(), bytearray()
    for xo, yo in ((8, 0), (8, 8), (0, 0), (0, 8)):
        for row in range(8):
            y = yo + row
            p0 = p1 = p2 = p3 = 0
            for pix in range(8):
                ci = px[y][xo + pix] & 15
                p0 |= ((ci >> 0) & 1) << pix
                p1 |= ((ci >> 1) & 1) << pix
                p2 |= ((ci >> 2) & 1) << pix
                p3 |= ((ci >> 3) & 1) << pix
            c1 += bytes((p0, p1))
            c2 += bytes((p2, p3))
    return bytes(c1), bytes(c2)


def tile_blank() -> list[list[int]]:
    return [[0] * 16 for _ in range(16)]


def tile_wall() -> list[list[int]]:
    tile = [[3] * 16 for _ in range(16)]
    for y in range(16):
        for x in range(16):
            if y in (0, 8) or x in (0, 8):
                tile[y][x] = 1
            elif y in (1, 9):
                tile[y][x] = 4
            elif (x + y) & 4:
                tile[y][x] = 2
    return tile


def tile_player() -> list[list[int]]:
    tile = [[0] * 16 for _ in range(16)]
    for y in range(16):
        for x in range(16):
            dx = x - 7
            if 2 <= y <= 13 and -5 <= dx <= 5:
                tile[y][x] = 9
            if 4 <= y <= 7 and -3 <= dx <= 3:
                tile[y][x] = 11
            if y in (5, 6) and abs(dx) <= 1:
                tile[y][x] = 15
            if y >= 10 and abs(dx) <= 2:
                tile[y][x] = 12
    return tile


def tile_marker() -> list[list[int]]:
    tile = [[0] * 16 for _ in range(16)]
    for i in range(16):
        tile[7][i] = 15
        tile[8][i] = 15
        tile[i][7] = 15
        tile[i][8] = 15
    return tile


def build_srom() -> bytes:
    data = bytearray(S_PAD)
    for code in range(128):
        glyph = GLYPHS.get(chr(code), GLYPHS[" "])
        offset = code * 32
        data[offset : offset + 32] = encode_fix_glyph(glyph)
    return bytes(data)


def build_croms() -> tuple[bytes, bytes]:
    c1, c2 = bytearray(), bytearray()
    for tile in (tile_blank(), tile_wall(), tile_player(), tile_marker()):
        a, b = encode_tile(tile)
        c1 += a
        c2 += b
    c1 += bytes(C_PAD - len(c1))
    c2 += bytes(C_PAD - len(c2))
    return bytes(c1), bytes(c2)


def main() -> int:
    parser = argparse.ArgumentParser(description="Generate ASM demo ROM graphics.")
    parser.add_argument("--out-dir", type=Path, required=True)
    args = parser.parse_args()
    args.out_dir.mkdir(parents=True, exist_ok=True)
    c1, c2 = build_croms()
    outputs = {
        "c1.bin": c1,
        "c2.bin": c2,
        "s1.bin": build_srom(),
        "v1.bin": bytes(V_PAD),
    }
    for name, blob in outputs.items():
        (args.out_dir / name).write_bytes(blob)
    print(f"gen_asm_gfx.py: wrote ASM asset ROMs to {args.out_dir}")
    return 0


